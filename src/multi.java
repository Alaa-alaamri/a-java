public class multi {

    public static void main(String[] args) {

        for (int i = 1; i <= 12; i++){
            for (int x=1; x<= 12; x++){

                int z = i * x;

                System.out.print("\t"+i+"*"+x+"=" +z + "\t");
            }

            System.out.println();
        }
}
}
